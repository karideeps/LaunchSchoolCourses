# !=        is a comparative operator that means NOT equal to. Returns true or false. Ex: 2 != 3 returns true
# ! before something, means NOT something. It is called the 'bang' operator. !variable means the opposite of the value store in the variable. Returns true or false
# ! after something is a ruby convention that generally states that the preceeding method will mutate the variable that called the method
# ? before something is the ternary operator for if...else
# ? after something is a ruby convention that generally states that the preceeding method will return a boolean
# !! before something means double NOT. I.e., returns the true or false value of the variable. I.e, converts the variable into its boolean equivalent

