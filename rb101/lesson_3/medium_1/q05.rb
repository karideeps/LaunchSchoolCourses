# the limit variable is define outside the scope of the method, and hence could not be used by the method
# there are 3 possible fixes

# Fix 1
# make define limit as a constant variable
LIMIT = 15

def fib(first_num, second_num)
  while first_num + second_num < LIMIT
    sum = first_num + second_num
    first_num = second_num
    second_num = sum
  end
  sum
end

result = fib(0, 1)
puts "result is #{result}"

# Fix 2
# pass limit as an argument to the method
limit = 15

def fib2(first_num, second_num, limit)
  while first_num + second_num < limit
    sum = first_num + second_num
    first_num = second_num
    second_num = sum
  end
  sum
end

result = fib2(0, 1, limit)
puts "result is #{result}"

# Fix 3
# define limit inside the method
def fib(first_num, second_num)
  limit = 15
  while first_num + second_num < limit
    sum = first_num + second_num
    first_num = second_num
    second_num = sum
  end
  sum
end

result = fib(0, 1)
